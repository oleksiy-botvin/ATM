package ua.edu.ztu.student.zipz221_boyu.data.use_case;

import androidx.annotation.NonNull;

/**
 * Базовий інтерфейс для варіантів використання, що не потребують вхідних параметрів.
 *
 * Визначає загальний контракт для операцій, які не приймають аргументів
 * та повертають результат типу R. Використовується як частина архітектурного
 * шаблону Use Case (Interactor) в Clean Architecture.
 *
 * Типові випадки використання:
 * - Отримання загального балансу банкомату
 * - Запит на технічне обслуговування
 * - Отримання списку доступних операцій
 * - Отримання статусу системи
 *
 * @param <R> тип результату операції
 *
 * @see WithArgUseCase для операцій з вхідними параметрами
 */
public interface WithoutArgUseCase<R> {

    /**
     * Виконує операцію без параметрів.
     *
     * @return результат виконання операції
     */
    @NonNull R invoke();
}
