package ua.edu.ztu.student.zipz221_boyu.data.use_case;

import androidx.annotation.NonNull;

/**
 * Базовий інтерфейс для варіантів використання, що потребують вхідних параметрів.
 *
 * Визначає загальний контракт для операцій, які приймають аргумент типу A
 * та повертають результат типу R. Використовується як частина архітектурного
 * шаблону Use Case (Interactor) в Clean Architecture.
 *
 * Типові випадки використання:
 * - Операції з конкретними банківськими рахунками
 * - Транзакції з параметрами
 * - Операції автентифікації з обліковими даними
 *
 * @param <A> тип вхідного аргументу операції
 * @param <R> тип результату операції
 *
 * @see WithoutArgUseCase для операцій без вхідних параметрів
 */
public interface WithArgUseCase<A, R> {

    /**
     * Виконує операцію з заданим аргументом.
     *
     * @param arg вхідний аргумент операції
     * @return результат виконання операції
     * @throws NullPointerException якщо arg є null
     */
    @NonNull R invoke(@NonNull A arg);
}
