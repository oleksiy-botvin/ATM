package ua.edu.ztu.student.zipz221_boyu.data.repository;

import androidx.annotation.NonNull;

/**
 * Базовий інтерфейс для репозиторіїв, що потребують вхідних параметрів.
 *
 * Визначає загальний контракт для доступу до даних, де операції приймають
 * аргумент типу A та повертають результат типу R. Використовується як частина
 * архітектурного шаблону Repository в Clean Architecture.
 *
 * Типові випадки використання:
 * - Отримання інформації про конкретний банківський рахунок
 * - Пошук транзакцій за параметрами
 * - Отримання даних користувача за ідентифікатором
 * - Фільтрація та сортування даних за заданими критеріями
 *
 * @param <A> тип вхідного аргументу операції
 * @param <R> тип результату операції
 *
 * @see WithoutArgRepository для репозиторіїв без вхідних параметрів
 */
public interface WithArgRepository<A, R> {

    /**
     * Виконує операцію доступу до даних з заданим аргументом.
     *
     * @param arg вхідний аргумент операції
     * @return результат виконання операції
     * @throws NullPointerException якщо arg є null
     */
    @NonNull R invoke(@NonNull A arg);
}
